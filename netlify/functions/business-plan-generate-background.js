const crypto = require("crypto");
const { json, badMethod } = require("./_lib/http");
const { getPool } = require("./_lib/db");
const { paystackVerifyTransaction } = require("./_lib/payments");
const {
  ensureBusinessPlanTables,
  findOrderByReference,
  markOrderPaid,
  markPlanGenerated,
  markPlanFailed,
  markEmailSent,
  markGeneratedEmailSent,
  newPlanUuid,
} = require("./_lib/business-plans");
const {
  ensureStudentAuthTables,
  findStudentByEmail,
  createStudentAccount,
} = require("./_lib/user-auth");
const { sendEmail } = require("./_lib/email");

function clean(value, max) {
  return String(value || "").trim().slice(0, max);
}

function randomPassword() {
  return crypto.randomBytes(6).toString("base64url") + "A9!";
}

function webhookBase() {
  return String(process.env.N8N_BASE_URL || process.env.NEXT_PUBLIC_N8N_BASE_URL || "").trim();
}

function webhookPath() {
  return String(process.env.BUSINESS_PLAN_WEBHOOK_PATH || "/webhook/linescout_business_plan").trim();
}

function webhookUrl() {
  const path = webhookPath();
  if (/^https?:\/\//i.test(path)) return path;

  const base = webhookBase();
  if (!base) throw new Error("N8N_BASE_URL is not configured.");

  const cleanBase = String(base).replace(/\/$/, "");
  const cleanPath = String(path || "").startsWith("/") ? String(path) : `/${String(path || "")}`;
  return `${cleanBase}${cleanPath}`;
}

function dashboardPlansUrl() {
  const base = String(process.env.SITE_BASE_URL || "https://tochukwunkwocha.com").trim().replace(/\/$/, "");
  return `${base}/dashboard/business-plans/`;
}

function buildAwaitingVerificationEmail({ fullName, dashboardUrl, tempPassword }) {
  const safeName = clean(fullName, 120) || "there";
  const credentials = tempPassword
    ? `<p><strong>Your dashboard account was created automatically.</strong><br/>Temporary password: <code>${tempPassword}</code><br/>You must reset this password on first login.</p>`
    : `<p>Your existing dashboard account has been linked to this business plan.</p>`;

  const html = [
    `<p>Hello ${safeName},</p>`,
    `<p>Your business plan has been generated by our AI and is currently awaiting expert verification.</p>`,
    credentials,
    `<p>Open your dashboard plans here: <a href="${dashboardUrl}">${dashboardUrl}</a></p>`,
    `<p>Once verified, we will send another email and enable download in your dashboard.</p>`,
  ].join("\n");

  const text = [
    `Hello ${safeName},`,
    "",
    "Your business plan has been generated by our AI and is currently awaiting expert verification.",
    tempPassword
      ? `Your dashboard account was created automatically. Temporary password: ${tempPassword}. You must reset this password on first login.`
      : "Your existing dashboard account has been linked to this business plan.",
    `Open your dashboard plans here: ${dashboardUrl}`,
    "Once verified, we will send another email and enable download in your dashboard.",
  ].join("\n");

  return { html, text };
}

async function generatePlan(order) {
  let intake = {};
  try {
    intake = order.intake_json ? JSON.parse(order.intake_json) : {};
  } catch (_error) {
    intake = {};
  }

  const payload = {
    type: "business_plan",
    purpose: clean(order.purpose, 40) || "loan",
    currency: clean(order.currency, 16) || "NGN",
    exchangeRate: order.exchange_rate ? Number(order.exchange_rate) : undefined,
    format: "both",
    intake,
    payment: {
      provider: "paystack",
      reference: clean(order.payment_reference, 120),
      amountMinor: Number(order.payment_amount_minor || 0),
      email: clean(order.email, 190),
    },
  };

  const res = await fetch(webhookUrl(), {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(payload),
  });

  const data = await res.json().catch(() => null);
  if (!res.ok || !data || !data.planText || !String(data.planText).trim()) {
    throw new Error((data && (data.error || data.message)) || "Business plan generation failed.");
  }
  return { planText: String(data.planText) };
}

exports.handler = async function (event) {
  if (event.httpMethod !== "POST") return badMethod();

  let body;
  try {
    body = JSON.parse(event.body || "{}");
  } catch (_error) {
    return json(400, { ok: false, error: "Invalid JSON body" });
  }

  const reference = clean(body.reference, 120);
  const forceRegenerate = Boolean(body.forceRegenerate);
  if (!reference) return json(400, { ok: false, error: "reference is required" });

  const pool = getPool();

  try {
    await ensureBusinessPlanTables(pool);
    await ensureStudentAuthTables(pool);

    let order = await findOrderByReference(pool, reference);
    if (!order) return json(404, { ok: false, error: "Payment order not found." });

    if (String(order.payment_status || "").toLowerCase() !== "paid") {
      const tx = await paystackVerifyTransaction(reference);
      if (String(tx.status || "").toLowerCase() !== "success") {
        return json(400, { ok: false, error: "Payment not successful." });
      }
      await markOrderPaid(pool, {
        id: order.id,
        paymentReference: reference,
        providerOrderId: tx.id ? String(tx.id) : null,
        paymentCurrency: clean(tx.currency, 16) || "NGN",
        paymentAmountMinor: Number(tx.amount || 0),
      });
      order = await findOrderByReference(pool, reference);
      if (!order) return json(404, { ok: false, error: "Payment order not found after payment update." });
    }

    if (!forceRegenerate && String(order.plan_status || "").toLowerCase() === "generated" && String(order.plan_text || "").trim()) {
      return json(200, { ok: true, alreadyGenerated: true, planUuid: clean(order.plan_uuid, 64) || null });
    }

    const generated = await generatePlan(order);

    let account = await findStudentByEmail(pool, order.email);
    let tempPassword = "";
    let accountCreated = false;
    if (!account) {
      tempPassword = randomPassword();
      account = await createStudentAccount(pool, {
        fullName: clean(order.full_name, 180),
        email: clean(order.email, 190),
        password: tempPassword,
        mustResetPassword: true,
      });
      accountCreated = true;
    }

    const planUuid = await markPlanGenerated(pool, {
      id: order.id,
      planUuid: newPlanUuid(),
      planText: generated.planText,
      accountId: account && account.id ? account.id : null,
    });

    if (!order.generated_email_sent_at) {
      const dashboardUrl = dashboardPlansUrl();
      const emailPayload = buildAwaitingVerificationEmail({
        fullName: clean(order.full_name, 180),
        dashboardUrl,
        tempPassword: accountCreated ? tempPassword : "",
      });
      try {
        await sendEmail({
          to: clean(order.email, 190),
          subject: "Your Business Plan Is Awaiting Expert Verification",
          html: emailPayload.html,
          text: emailPayload.text,
        });
        await markEmailSent(pool, order.id);
        await markGeneratedEmailSent(pool, order.id);
      } catch (emailError) {
        console.error(
          "business-plan-generate-background generated email send failed:",
          emailError && emailError.message ? emailError.message : emailError
        );
        // Non-blocking for generation success
      }
    }

    return json(200, {
      ok: true,
      planUuid,
      regenerated: forceRegenerate,
      verificationStatus: "awaiting_verification",
    });
  } catch (error) {
    try {
      const order = await findOrderByReference(pool, reference);
      if (order && order.id) {
        await markPlanFailed(pool, { id: order.id, error: error.message || "Unknown error" });
      }
    } catch (_error) {}
    return json(500, { ok: false, error: error.message || "Could not generate business plan." });
  }
};
